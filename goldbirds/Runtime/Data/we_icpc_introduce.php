<h2><span class="mw-headline" id=".E5.8C.BA.E5.9F.9F.E5.92.8C.E5.85.A8.E7.90.83.E5.86.B3.E8.B5.9B">区域和全球决赛</span></h2>
<p>赛事由各大洲区域预赛（regional）和全球总决赛（worldfinal）两个主要阶段组成。根据各赛区规则，每站前若干名的学校自动获得参加全球总决赛的资格。决赛安排在每年的3-4月举行，而区域预赛一般安排在上一年的9-12月举行。一个大学可以有多支队伍参加区域预赛，但只能有一支队伍参加全球总决赛。</p>
<p>全球总决赛第一名将获得奖杯一座。另外，成绩靠前的参赛队伍也将获得金、银和铜牌。而解题数在中等以下的队伍会得到确认但不会进行排名。</p>
