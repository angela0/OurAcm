<h3><span class="mw-headline" id=".E4.B8.96.E7.95.8C.E6.80.BB.E5.86.B3.E8.B5.9B">世界总决赛</span></h3>
<p>由上海交通大学承办的第29届ACM国际大学生程序设计竞赛全球总决赛于2005年4月3-7日期间在上海浦东的香格里拉酒店举行。总共有78支队伍参加总决赛的角逐，它们是从来自6大洲71个国家1582所大学的4109支队伍中通过之前的各级区域预赛中脱颖而出的。在当地时间4月6日上午举行的全球总决赛上，东道主上海交通大学代表队是所有参赛队伍中唯一一支解出8道题目的队伍，从而在时隔3年之后第二次夺得全球总冠军。</p>
<p>由哈尔滨工程大学承办的第34届ACM国际大学生程序设计竞赛全球总决赛于2010年2月在哈尔滨举行，上海交通大学再次夺冠。</p>
<h2><span class="mw-headline" id=".E6.9C.80.E8.BF.91.E7.9A.84.E6.AF.94.E8.B5.9B">最近的比赛</span></h2>
<p>2007年ACM国际大学生程序设计竞赛全球总决赛于日本的东京举行。波兰的华沙大学获冠军，清华大学获得亚军。</p>
<p>2008年ACM国际大学生程序设计竞赛全球总决赛于2008年4月6日－10日在阿尔贝塔的班夫举行<sup id="cite_ref-2" class="reference"><a href="#cite_note-2">[2]</a></sup>。圣彼得堡的光学和精密机械研究院获得了他们的第二次世界冠军。</p>
<p>2009年ACM国际大学生程序设计竞赛全球总决赛于2009年4月18日－22日在瑞典的斯德哥尔摩举行。圣彼得堡的光学和精密机械研究院获得了他们的第三次世界冠军。清华大学再次获得亚军。</p>
<p>2010年ACM国际大学生程序设计竞赛全球总决赛于2010年2月1日－6日在中国的哈尔滨工程大学举行。中国的上海交通大学获得了他们的第三次世界冠军。</p>
<p>2011年ACM国际大学生程序设计竞赛全球总决赛于2011年5月29日－30日在美国奥兰多举行。来自中国的浙江大学获得了世界冠军。</p>
<p>2012年ACM国际大学生程序设计竞赛全球总决赛于2012年5月13日－18日在波兰华沙举行。来自俄罗斯的圣彼得堡国立资讯科技、机械与光学大学获得了第四次冠军，是第一支取得四次冠军的学校。</p>
<p>2013年ACM国际大学生程序设计竞赛全球总决赛于2013年7月2日－3日在俄罗斯圣彼得堡举行。来自俄罗斯的圣彼得堡国立资讯科技、机械与光学大学获得了第五次冠军，是第一支取得五次冠军的学校。</p>
